
let atendimento = 2

switch (atendimento) {
    case 2:
        console.log(`Ótimo`)
        break;
    case 3:
        console.log(`Bom`)
        break;
    case 4:
        console.log(`Recular`)
        break;
    case 5:
        console.log(`Ruim`)
        break;
    case 6:
        console.log(`Péssimo`)
        break;
    default: 
        console.log(`Opção inválida`)
        break; //opcional no default
}